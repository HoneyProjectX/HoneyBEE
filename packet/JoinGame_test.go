func TestJoinGameNBT(T *testing.T) {
	JG := new(JoinGame_CB)
	PW := CreatePacketWriter(0x00)
	JG.Encode(&PW)
	JGDC := JG.DimensionCodec
	if len(JGDC) != len(TestJoinGameCodecData) {
		T.Error(nbt.NBTLength)
		fmt.Print("DCL: ", len(JGDC), " TJD: ", len(TestJoinGameCodecData))
	} else {
		for i := 0; i < len(TestJoinGameCodecData); i++ {
			if TestJoinGameCodecData[i] != JGDC[i] {
				T.Error(nbt.NBTValue)
				T.Error(i)
				T.Error(TestJoinGameCodecData[i])
				T.Error(JGDC[i])
				return
			}
		}
	}
}
